export function getBook({isArray}: { isArray?: boolean } = {isArray: false}) {
    if (!isArray) {
        return book;
    }

    return Object.entries(book).reduce((acc, [pageId, page]) => {
        return [
            ...acc,
            ...Object.entries(page).map(([lineId, line]) => ({
                page: parseInt(pageId, 10),
                line: parseInt(lineId, 10),
                ...line
            }))
        ];
    }, [] as BookArray);
}

export function getPage({page, isArray}: { page: number, isArray?: boolean }) {
    if (!isArray) {
        return book[page];
    }
    return (getBook({isArray: true}) as BookArray)
        .filter(item => item.page === page)
}

export function getLines({line, isArray}: { line: number, isArray?: boolean }) {
    if (!isArray) {
        return Object.entries(book)
            .map(([key, page]) => ({
                key,
                value: page[line]
            }))
            .reduce((acc, page) =>
                ({
                    ...acc,
                    [page.key]: page.value,
                }), {});
    }
    return (getBook({isArray: true}) as BookArray)
        .filter(item => item.line === line)

}

export function getLine({page, line, isArray}: { page: number, line: number, isArray?: boolean }) {
    if(!isArray) {
        return book[page][line];
    }

    return [{
        page,
        line,
        ...book[page][line]
    }];
}


